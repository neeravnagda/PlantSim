#ifndef BRANCH_H_
#define BRANCH_H_

#include <string>
#include <vector>
#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @file Branch.h
/// @brief This struct is used to contain precomputed branch information
/// @author Neerav Nagda
/// @version 3.0
/// @date 08/05/17
/// @struct Branch
/// @brief Struct to contain precomputed branch information for drawing
//----------------------------------------------------------------------------------------------------------------------
typedef struct Branch
{
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Depth of tree when branch was created
		//----------------------------------------------------------------------------------------------------------------------
		unsigned m_creationDepth;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief L-system string
		//----------------------------------------------------------------------------------------------------------------------
		std::string m_string;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Positions of the branch nodes
		//----------------------------------------------------------------------------------------------------------------------
		std::vector<ngl::Vec3> m_nodePositions;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Positions of the leaves - this is the position where the leaf connects to the branch
		//----------------------------------------------------------------------------------------------------------------------
		std::vector<ngl::Vec3> m_leafPositions;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Rotations of the leaves
		//----------------------------------------------------------------------------------------------------------------------
		std::vector<ngl::Vec3> m_leafRotations;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Constructor
		//----------------------------------------------------------------------------------------------------------------------
		Branch(unsigned _depth, std::string _string = "") :
			m_creationDepth(_depth),
			m_string(_string){}
} Branch;

#endif // BRANCH_H_
