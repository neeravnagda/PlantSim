#ifndef BRANCH_H_
#define BRANCH_H_

#include <string>
#include <vector>
#include <boost/uuid/uuid.hpp>
#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @file Branch.h
/// @brief This struct is used to contain precomputed branch information
/// @author Neerav Nagda
/// @version 2.0
/// @date 10/04/17
/// @struct Branch
/// @brief Struct to contain precomputed branch information for drawing
//----------------------------------------------------------------------------------------------------------------------
typedef struct Branch
{
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief ID of the branch
		//----------------------------------------------------------------------------------------------------------------------
		boost::uuids::uuid m_ID;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Depth of tree when branch was created
		//----------------------------------------------------------------------------------------------------------------------
		unsigned m_creationDepth;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief L-system string
		//----------------------------------------------------------------------------------------------------------------------
		std::string m_string;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Positions of the branch nodes
		//----------------------------------------------------------------------------------------------------------------------
		std::vector<ngl::Vec3> m_positions;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Constructor
		//----------------------------------------------------------------------------------------------------------------------
		Branch(boost::uuids::uuid _ID, unsigned _depth, std::string _string = "") :
			m_ID(_ID),
			m_creationDepth(_depth),
			m_string(_string){}
} Branch;

#endif // BRANCH_H_
