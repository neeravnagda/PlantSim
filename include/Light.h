#ifndef LIGHT_H_
#define LIGHT_H_

#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @file Light.h
/// @brief This struct contains light information
/// @author Neerav Nagda
/// @version 1.0
/// @date 04/04/17
/// @struct Light
/// @brief Contains light information used by the shaders
//----------------------------------------------------------------------------------------------------------------------
typedef struct Light
{
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Check if the light is active
		//----------------------------------------------------------------------------------------------------------------------
		bool m_isActive = false;
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Position of the light
		//----------------------------------------------------------------------------------------------------------------------
		ngl::Vec3 m_position = ngl::Vec3::zero();
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Ambient colour
		//----------------------------------------------------------------------------------------------------------------------
		ngl::Vec3 m_ambient = ngl::Vec3(0.5f,0.5f,0.5f);
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Diffuse colour
		//----------------------------------------------------------------------------------------------------------------------
		ngl::Vec3 m_diffuse = ngl::Vec3(1.0f,1.0f,1.0f);
		//----------------------------------------------------------------------------------------------------------------------
		/// @brief Specular colour
		/// //----------------------------------------------------------------------------------------------------------------------
		ngl::Vec3 m_specular = ngl::Vec3(1.0f,1.0f,1.0f);
} Light;

#endif // LIGHT_H_
